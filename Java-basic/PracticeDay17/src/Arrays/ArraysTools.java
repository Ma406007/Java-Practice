package Arrays;

//Arrays:操作数组的工具类

public class ArraysTools {
    public static void main(String[] args) {
        //public static String toString(数组) 把数组拼接成一个字符串
        //public static int binarySearch(数组, 查找的元素) 二分查找法查找元素(数组必须是升序排序,元素存在返回索引,元素不存在返回-插入点-1,插入点是查找的元素如果存在应该在的位置)
        //public static int[] copyOf(原数组, 新数组长度) 拷贝数组(新数组长度小于老数组会部分拷贝,等于老数组会完全拷贝,大于老数组会完全拷贝并对多余的元素初始化为默认值)
        //public static int[] copyOfRange(原数组, 起始索引, 结束索引) 拷贝数组(指定范围,包头不包尾)
        //public static void fill(数组, 元素) 填充数组
        //public static void sort(数组) 按照默认方式进行数组排序(底层快排)
        //public static void sort(数组, 排序规则) 按照指定的规则进行排序
    }
}
