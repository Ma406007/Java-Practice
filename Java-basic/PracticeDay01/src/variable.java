public class variable {
    public static void main(String[] args) {
        // 定义变量
        // 数据类型 变量名 = 数据值;

        // 1.输出打印
        int a = 10;
        System.out.println(a);

        // 2.参与计算
        int a1 = 10;
        int b = 20;
        System.out.println(a1+b);

        // 3.修改已定义变量的值
        a = 50;
        System.out.println(a);

        // 4.一条语句中可定义多个变量
        int d = 100, e = 200, f = 300;
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
